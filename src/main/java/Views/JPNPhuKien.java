    public String tenSanPham(String ma){
        List<SanPham> pk = sanPhamService.getList();
        for (SanPham x : pk) {
            if(x.getMaSanPham().equals(ma)){
                return x.getTenSanPham();
            }
        }
        return null;
    }
    public String maSanPham(String ma){
        List<SanPham> pk = sanPhamService.getList();
        for (SanPham x : pk) {
            if(x.getTenSanPham().equals(ma)){
                return x.getMaSanPham();
            }
        }
        return null;
    }

