    private void tbl_phukienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_phukienMouseClicked
        int index = tbl_phukien.getSelectedRow();
        PhuKien x = (PhuKien) phuKienService.getList().get(index);
        txt_ma.setText(x.getMaPhuKien());
        txt_dientro.setText(x.getDienTro());
        txt_giaban.setText(String.valueOf(x.getGiaBan()));
        txt_gianhap.setText(String.valueOf(x.getGiaNhap()));
        txt_soluong.setText(String.valueOf(x.getSoLuong()));
        txt_ten.setText(x.getTenPhuKien());
        cbc_sanpham.setSelectedItem(tenSanPham(x.getMaSanPham().getMaSanPham()));
    }//GEN-LAST:event_tbl_phukienMouseClicked

