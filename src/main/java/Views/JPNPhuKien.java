    public void loadTable(List<PhuKien> pk){
        defaultTableModel = (DefaultTableModel) tbl_phukien.getModel();
        defaultTableModel.setRowCount(0);
        for (PhuKien x : pk) {
            defaultTableModel.addRow(new Object[]{x.getMaPhuKien(),x.getTenPhuKien(),x.getSoLuong(),x.getGiaNhap(),x.getGiaBan(),x.getDienTro(),tenSanPham(x.getMaSanPham().getMaSanPham())});
        }
    }
    public void cbcTenSanPham(List<SanPham> sp){
        for (SanPham x : sp) {
            defaultComboBoxModel.addElement(tenSanPham(x.getMaSanPham()));
        }
        cbc_sanpham.setModel(defaultComboBoxModel);
    }
