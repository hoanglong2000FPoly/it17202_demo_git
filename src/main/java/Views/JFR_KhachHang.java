    public List<KhachHang> getList() {
        List<KhachHang> khachhangs;
        try (Session session = HibernateUtil.getSessionFactory().openSession()) {
        TypedQuery<KhachHang> kh = session.createQuery("SELECT a FROM KhachHang a");
        khachhangs = kh.getResultList();
        }
        return khachhangs;
    }
    
    public void loadData(List<KhachHang> lst) {
        dTable = (DefaultTableModel) tbl_Table.getModel();
        dTable.setRowCount(0);
        
        for (KhachHang x : lst) {
            dTable.addRow(new Object[] {x.getMaKhachHang(), x.getTenKhachHang(), x.getGioiTinh()==0?"Nam":"Nữ", x.getEmail(), x.getSDT()});
        }
    }
    
    KhachHangView GUIData() {
        return new KhachHangView(-1, txt_TenKH.getText(), rdb_Nam.isSelected()?0:1, txt_Email.getText(), txt_SDT.getText());
    }
    
    
    
