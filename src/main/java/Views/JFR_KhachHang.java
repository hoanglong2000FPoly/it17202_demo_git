    private void tbl_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_TableMouseClicked
        int i = tbl_Table.getSelectedRow();
        
        KhachHang kh = getList().get(i);
        txt_MaKH.setText(String.valueOf(kh.getMaKhachHang()));
        txt_TenKH.setText(kh.getTenKhachHang());
        rdb_Nam.setSelected(kh.getGioiTinh()==0?true:false);
        rdb_Nu.setSelected(kh.getGioiTinh()==1?true:false);
        txt_Email.setText(kh.getEmail());
        txt_SDT.setText(kh.getSDT());
    }//GEN-LAST:event_tbl_TableMouseClicked

    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        int i = JOptionPane.showConfirmDialog(this, "Bạn muốn thêm không?");
        if (i == 0) {
            JOptionPane.showMessageDialog(this, khService.add(GUIData()));
            loadData(getList());
        }
    }//GEN-LAST:event_btn_ThemActionPerformed

